package objects;

import javafx.scene.Parent;

public class BusinessInfo {
    String businessName;
    String businessRegistrationParentType;
    String businessRegistrationType;
    String businessRegistrationNumber;
    String businessWebsite;
    String businessCountry;
    String businessIndustry;
    String businessSubIndustry;

    public String getBusinessName() {
        return businessName;
    }

    public void setBusinessName(String businessName) {
        this.businessName = businessName;
    }

    public String getBusinessRegistrationParentType() {
        return businessRegistrationParentType;
    }

    public void setBusinessRegistrationParentType(String businessRegistrationParentType) {
        this.businessRegistrationParentType = businessRegistrationParentType;
    }

    public String getBusinessRegistrationType() {
        return businessRegistrationType;
    }

    public void setBusinessRegistrationType(String businessRegistrationType) {
        this.businessRegistrationType = businessRegistrationType;
    }

    public String getBusinessRegistrationNumber() {
        return businessRegistrationNumber;
    }

    public void setBusinessRegistrationNumber(String businessRegistrationNumber) {
        this.businessRegistrationNumber = businessRegistrationNumber;
    }

    public String getBusinessWebsite() {
        return businessWebsite;
    }

    public void setBusinessWebsite(String businessWebsite) {
        this.businessWebsite = businessWebsite;
    }

    public String getBusinessCountry() {
        return businessCountry;
    }

    public void setBusinessCountry(String businessCountry) {
        this.businessCountry = businessCountry;
    }

    public String getBusinessIndustry() {
        return businessIndustry;
    }

    public void setBusinessIndustry(String businessIndustry) {
        this.businessIndustry = businessIndustry;
    }

    public String getBusinessSubIndustry() {
        return businessSubIndustry;
    }

    public void setBusinessSubIndustry(String businessSubIndustry) {
        this.businessSubIndustry = businessSubIndustry;
    }
}
